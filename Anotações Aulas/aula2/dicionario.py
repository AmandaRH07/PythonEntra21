# criar dicionario chave:valor
dicionario = {'color': 'blue', 'pet': 'dog', 'fruit': 'apple'}
print(dicionario)

# iterando com chaves
for key in dicionario:
    print(key, " -> ", dicionario[key])

# iterando sobre os itens
for item in dicionario.items():
    print(item)

# chaves e valores
print(dicionario.keys())
print(dicionario.values())
print("blue" in dicionario.values())

